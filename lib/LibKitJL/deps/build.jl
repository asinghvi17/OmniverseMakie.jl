# Build libkitjl.so by g++-compiling src/kitjl_shim.cpp against the located Kit
# release dir's dev headers, linking only -lcarb.  The omni.kit.app plugin and
# the composite extension chain are loaded at RUNTIME by the framework, not at
# link time.
#
# GRACEFUL DEGRADATION (load-bearing): Kit is NVIDIA-proprietary and located
# via KIT_RELEASE_DIR — it is not present on every machine.  If the release dir
# or its dev headers are absent, this build MUST NOT fail the workspace
# precompile; it writes an "unavailable" marker into deps/deps.jl instead, and
# LibKitJL.__init__ warns that the in-process transport is unavailable (the
# subprocess transport still works).  This mirrors how ovrtx is env-located
# rather than vendored.

const DEPS_DIR = @__DIR__
const DEPS_JL = joinpath(DEPS_DIR, "deps.jl")

_default_kit_release_dir() = get(ENV, "KIT_RELEASE_DIR",
    joinpath(homedir(),
        "temp/omniverse-dsx-blueprint-for-ai-factories/deps/kit-cae/_build/linux-x86_64/release"))

function write_unavailable(reason::AbstractString)
    open(DEPS_JL, "w") do io
        println(io, "# Auto-generated by LibKitJL/deps/build.jl — DO NOT EDIT.")
        println(io, "# In-process Kit transport unavailable at build time.")
        println(io, "const LIBKITJL_AVAILABLE = false")
        println(io, "const LIBKITJL_PATH = \"\"")
        println(io, "const LIBKITJL_KIT_RELEASE_DIR = \"\"")
        println(io, "const LIBKITJL_UNAVAILABLE_REASON = ", repr(String(reason)))
    end
    @info "LibKitJL: in-process transport unavailable; wrote marker (subprocess transport unaffected)" reason
    return nothing
end

function write_available(libpath::AbstractString, kit_release_dir::AbstractString)
    open(DEPS_JL, "w") do io
        println(io, "# Auto-generated by LibKitJL/deps/build.jl — DO NOT EDIT.")
        println(io, "const LIBKITJL_AVAILABLE = true")
        println(io, "const LIBKITJL_PATH = ", repr(String(libpath)))
        println(io, "const LIBKITJL_KIT_RELEASE_DIR = ", repr(String(kit_release_dir)))
        println(io, "const LIBKITJL_UNAVAILABLE_REASON = \"\"")
    end
    @info "LibKitJL: built libkitjl.so" libpath kit_release_dir
    return nothing
end

function build()
    kit = _default_kit_release_dir()
    inc = joinpath(kit, "kit", "dev", "include")
    fabric_inc = joinpath(kit, "kit", "dev", "fabric", "include")
    kitdir = joinpath(kit, "kit")
    libcarb = joinpath(kitdir, "libcarb.so")

    isdir(kit) || return write_unavailable("KIT_RELEASE_DIR not found: $kit")
    isdir(inc) || return write_unavailable("Kit dev headers not found: $inc")
    isfile(joinpath(inc, "omni", "kit", "IApp.h")) ||
        return write_unavailable("omni/kit/IApp.h not found under $inc")
    isfile(libcarb) || return write_unavailable("libcarb.so not found: $libcarb")

    cxx = get(ENV, "CXX", "/usr/bin/g++")
    Sys.which(cxx) === nothing && isfile(cxx) ||
        (Sys.which(cxx) === nothing && return write_unavailable("C++ compiler not found: $cxx"))

    src = joinpath(dirname(DEPS_DIR), "src", "kitjl_shim.cpp")
    out = joinpath(DEPS_DIR, "libkitjl.so")

    cmd = `$cxx -std=c++17 -O2 -fPIC -shared
        -I$inc -I$fabric_inc
        $src -o $out
        -L$kitdir -lcarb -Wl,-rpath,$kitdir`

    @info "LibKitJL: compiling libkitjl.so" cmd
    try
        run(cmd)
    catch e
        return write_unavailable("g++ compile failed: $e")
    end
    isfile(out) || return write_unavailable("compile produced no libkitjl.so at $out")
    return write_available(out, kit)
end

build()
